"""
数值运算
    注意：
        1、Python可以同时为多个变量赋值，如a, b = 1, 2。
        2、一个变量可以通过赋值指向不同类型的对象。
        3、数值的除法包含两个运算符：/ 返回一个浮点数，// 返回一个整数。
        4、在混合计算时，Python会把整型转换成为浮点数。
"""
print("======数值运算")
# 加法
print(5 + 4)
# 减法
print(4.3 - 2)
# 乘法
print(3 * 7)
# 除法，得到一个浮点数
print(2 / 4)
# 除法，得到两个浮点数
print(2 // 4)
# 取余
print(17 % 3)
# 乘方
print(2 ** 5)

